    private void dropRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomButtonActionPerformed

        dropTextArea.setText("");

        //obtain room input and drop from room table
        String roomName = dropRoomComboBox.getSelectedItem().toString();
        RoomQueries.dropRoom(roomName);
        String status = "Room " + roomName + " has been dropped \n";

        //get reservations based on dropped room
        ArrayList<ReservationEntry> reservationByRoom = ReservationQueries.getReservationsByRoom(roomName);

        //loop through reservations of dropped room, delete reservation,
        //add reservation to waitlist, update status
        for (ReservationEntry reservation : reservationByRoom) {
            ReservationQueries.deleteReservationByRoom(roomName);
            WaitlistQueries.addWaitlistEntry(reservation.getFaculty(), reservation.getDate(), reservation.getSeats(), reservation.getTimestamp());
            status += (reservation.getFaculty() + " has been added to waitlist for " + reservation.getDate() + "\n");
        }
        
        //get waitlist
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getWaitlist();

        //loop through waitlist, obtain all possible room and rooms reserved for date
        for (WaitlistEntry entry : waitlist) {

            ArrayList<String> possibleRooms = RoomQueries.getAllPossibleRooms(entry.getSeats());
            ArrayList<String> roomReservations = ReservationQueries.getRoomsReservedByDate(entry.getDate());

            //loop for rooms that are not reserved --  if not reserved, reservation added from waitlist entry
            //status updated and added reservation deleted from waitlist
            for (String room : possibleRooms) {
                if (!roomReservations.contains(room)) {
                    ReservationQueries.addReservationEntry(entry.getFaculty(), room, entry.getDate(), entry.getSeats(), entry.getTimestamp());
                    status += (entry.getFaculty() + " has been reserved for room " + room + " on date " + entry.getDate() + "\n");
                    WaitlistQueries.deleteWaitlistEntry(entry.getFaculty(), entry.getDate());
                    break;
                }
            }
        }
        dropTextArea.append(status);
        rebuildRoomComboBox();

    }//GEN-LAST:event_dropRoomButtonActionPerformed

