    private void statusSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusSubmitButtonActionPerformed

        Date date = Date.valueOf(statusDateComboBox.getSelectedItem().toString());

        statusTextArea.setText("");

        //check which combo box was selected -- if reservation, get reservations of given date
        if (statusTypeComboBox.getSelectedItem() == "Reservations") {
            statusTextArea.append("Faculty:" + "\t\t" + "Room:" + "\n" + "\n");
            ReservationQueries.getReservationsByDate(date).forEach((reservation) -> {
                statusTextArea.append(reservation.getFaculty() + "\t\t" + reservation.getRoom() + "\n");
            }); //else if waitlist selected, get waitlist for all dates    
        } else {
            statusTextArea.append("Faculty:" + "\t\t" + "Date:" + "\t\t" + "Seats:" + "\n" + "\n");
            WaitlistQueries.getWaitlist().forEach((waitlist) -> {
                statusTextArea.append(waitlist.getFaculty() + "\t\t" + waitlist.getDate() + "\t\t" + waitlist.getSeats() + "\n");
            });
        }
    }//GEN-LAST:event_statusSubmitButtonActionPerformed

    private void addRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomButtonActionPerformed

        addTextArea.setText("");

        // obtain room/seats input and add room to room table
        String name = addRoomNameTextField.getText();
        String seats = addRoomSeatsTextField.getText();
        RoomQueries.addRoom(name, Integer.parseInt(seats));

        addRoomNameTextField.setText(null);
        addRoomSeatsTextField.setText(null);

        //search waitlist for entries, reserve room, remove from waitlist
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getWaitlist();
        String status = "Room " + name + " has been added \n";

        for (WaitlistEntry entry : waitlist) {
            ArrayList<String> possibleRooms = RoomQueries.getAllPossibleRooms(entry.getSeats());
            ArrayList<String> roomReservations = ReservationQueries.getRoomsReservedByDate(entry.getDate());

            //loop for rooms that are not reserved --  if not reserved, reservation added from waitlist entry
            //status updated and added reservation deleted from waitlist
            for (String room : possibleRooms) {
                if (!roomReservations.contains(room)) {
                    ReservationQueries.addReservationEntry(entry.getFaculty(), room, entry.getDate(), entry.getSeats(), entry.getTimestamp());
                    status += (entry.getFaculty() + " has been reserved for room " + room + " on date " + entry.getDate() + "\n");
                    WaitlistQueries.deleteWaitlistEntry(entry.getFaculty(), entry.getDate());
                    break;
                }
            }
        }
        //text area updated 
        addTextArea.append(status);
        rebuildRoomComboBox();


    }//GEN-LAST:event_addRoomButtonActionPerformed

