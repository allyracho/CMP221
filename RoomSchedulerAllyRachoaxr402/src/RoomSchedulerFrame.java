    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        String faculty = reserveFacultyComboBox.getSelectedItem().toString();
        Date date = Date.valueOf(reserveDateComboBox.getSelectedItem().toString());
        int seats = Integer.parseInt(reserveSeatsTextField.getText());
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        //call to possible rooms
        ArrayList<String> possibleRooms = RoomQueries.getAllPossibleRooms(seats);

        //call to reservations and get the rooms already reserved
        ArrayList<String> roomReservations = ReservationQueries.getRoomsReservedByDate(date);

        //loop for rooms that are not reserved --  if not reserved, reservation added 
        //else if reserved, waitlist entry added
        for (String room : possibleRooms) {
            if (!roomReservations.contains(room)) {
                ReservationQueries.addReservationEntry(faculty, room, date, seats, currentTimestamp);
                reserveStatusLabel.setText(faculty + " reserved room " + room);
                reserveSeatsTextField.setText(null);
                return;
            }

        }
        WaitlistQueries.addWaitlistEntry(faculty, date, seats, currentTimestamp);
        reserveStatusLabel.setText(faculty + " was added to waitlist for " + date);

        //reset seats textfield to null after entry added
        reserveSeatsTextField.setText(null);

        //rebuild comboboxes
        rebuildFacultyComboBoxes();
        rebuildDateComboBoxes();
    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

